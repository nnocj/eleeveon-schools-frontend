export default function CumulativeTranscriptCard({
  dataset,
  template,
  settings,
  compact = false,
  showWatermark = true,
  pageBreakAfter = true,
  mobilePreview = true,
  className = "",
}: CumulativeTranscriptCardProps) {
  const [expanded, setExpanded] = useState(false);
  const [fitScale, setFitScale] = useState(1);
  const [zoomScale, setZoomScale] = useState(1);
  const [zoomMenuOpen, setZoomMenuOpen] = useState(false);
  const previewFrameRef = useRef<HTMLDivElement | null>(null);
  const holdTimerRef = useRef<number | null>(null);
  const holdIntervalRef = useRef<number | null>(null);

  const resolvedSettings = useMemo(
    () => mergeTranscriptSettings({ template, settings, compact }),
    [template, settings, compact]
  );

  const selectedCode = templateCodeOf(template, resolvedSettings);
  const TemplateComponent = useMemo(
    () => resolveCumulativeTranscriptTemplateComponent(selectedCode),
    [selectedCode]
  );

  const previewScale = expanded ? zoomScale : fitScale;
  const displayZoomPercent = Math.round(previewScale * 100);
  const ZOOM_STEP = 1.01;

  const applyZoomStep = useCallback(
    (direction: "in" | "out") => {
      setZoomScale((prev) => {
        const baseScale = expanded ? prev : fitScale;
        const nextScale = direction === "in" ? baseScale * ZOOM_STEP : baseScale / ZOOM_STEP;
        return Math.min(2, Math.max(0.25, Number(nextScale.toFixed(4))));
      });
      setExpanded(true);
    },
    [expanded, fitScale]
  );

  const stopZoomHold = useCallback(() => {
    if (holdTimerRef.current != null) {
      window.clearTimeout(holdTimerRef.current);
      holdTimerRef.current = null;
    }

    if (holdIntervalRef.current != null) {
      window.clearInterval(holdIntervalRef.current);
      holdIntervalRef.current = null;
    }
  }, []);

  const startZoomHold = useCallback(
    (direction: "in" | "out") => {
      stopZoomHold();
      applyZoomStep(direction);

      holdTimerRef.current = window.setTimeout(() => {
        holdIntervalRef.current = window.setInterval(() => {
          applyZoomStep(direction);
        }, 55);
      }, 260);
    },
    [applyZoomStep, stopZoomHold]
  );

  const fitToScreen = () => {
    stopZoomHold();
    setExpanded(false);
    setZoomScale(1);
    setZoomMenuOpen(false);
  };

  const selectZoomPercent = (percent: number) => {
    stopZoomHold();
    setExpanded(true);
    setZoomScale(Number((percent / 100).toFixed(4)));
    setZoomMenuOpen(false);
  };

  useEffect(() => {
    if (!mobilePreview) return;

    const A4_WIDTH_PX = 793.7008;
    const A4_HEIGHT_PX = 1122.5197;
    const SAFE_GAP = 8;

    const updateScale = () => {
      const frame = previewFrameRef.current;
      if (!frame) return;

      const rect = frame.getBoundingClientRect();
      const availableWidth = Math.max(120, rect.width - SAFE_GAP);
      const availableHeight = Math.max(180, window.innerHeight - rect.top - SAFE_GAP);
      const widthScale = availableWidth / A4_WIDTH_PX;
      const heightScale = availableHeight / A4_HEIGHT_PX;
      const nextScale = Math.min(1, widthScale, heightScale);

      setFitScale(Number(nextScale.toFixed(4)));
    };

    updateScale();

    const observer = new ResizeObserver(updateScale);
    if (previewFrameRef.current) observer.observe(previewFrameRef.current);

    window.addEventListener("resize", updateScale);
    window.addEventListener("orientationchange", updateScale);

    return () => {
      observer.disconnect();
      window.removeEventListener("resize", updateScale);
      window.removeEventListener("orientationchange", updateScale);
    };
  }, [mobilePreview]);

  useEffect(() => {
    return () => stopZoomHold();
  }, [stopZoomHold]);

  const reportPage = dataset ? (
    <TemplateComponent
      dataset={dataset}
      template={template || null}
      settings={resolvedSettings}
      compact={compact}
      showWatermark={showWatermark}
      pageBreakAfter={pageBreakAfter}
      mobilePreview={false}
    />
  ) : (
    <section className="print-page report-page-break cumulative-transcript-empty-page ctc-a4-page">
      <div className="ctc-empty-card">{emptyTranscriptMessage(dataset)}</div>
    </section>
  );

  if (!mobilePreview) {
    return (
      <div className={className}>
        <style>{css}</style>
        {reportPage}
      </div>
    );
  }

  return (
    <div className={`ctc-preview-shell ${expanded ? "expanded" : ""} ${className}`}>
      <style>{css}</style>

      <div className="ctc-mobile-toolbar report-no-print">
        <div>
          <strong>{datasetStudentTitle(dataset)}</strong>
          <span>{datasetSubtitle(dataset)}</span>
        </div>

        <div className="ctc-zoom-controls" aria-label="Cumulative transcript zoom controls">
          <button
            type="button"
            className="ctc-zoom-icon-button"
            onClick={() => applyZoomStep("out")}
            onPointerDown={() => startZoomHold("out")}
            onPointerUp={stopZoomHold}
            onPointerCancel={stopZoomHold}
            onPointerLeave={stopZoomHold}
            aria-label="Zoom out"
            title="Click or hold to zoom out"
          >
            −
          </button>

          <button
            type="button"
            className="ctc-zoom-fit-button"
            onClick={fitToScreen}
            aria-label="Fit to screen"
            title="Fit to screen"
          >
            Fit
          </button>

          <div className="ctc-zoom-menu-wrap">
            <button
              type="button"
              className="ctc-zoom-percent-button"
              onClick={() => setZoomMenuOpen((prev) => !prev)}
              aria-label="Choose zoom percentage"
              aria-expanded={zoomMenuOpen}
              title="Choose zoom percentage"
            >
              <span>{displayZoomPercent}%</span>
              <span className="ctc-zoom-caret">▾</span>
            </button>

            {zoomMenuOpen && (
              <div className="ctc-zoom-menu" role="menu">
                {[30, 40, 50, 60, 70, 80, 90, 100].map((percent) => (
                  <button
                    key={percent}
                    type="button"
                    role="menuitem"
                    onClick={() => selectZoomPercent(percent)}
                    className={`ctc-zoom-menu-item ${Math.round(previewScale * 100) === percent ? "active" : ""}`}
                  >
                    {percent}%
                  </button>
                ))}
              </div>
            )}
          </div>

          <button
            type="button"
            className="ctc-zoom-icon-button"
            onClick={() => applyZoomStep("in")}
            onPointerDown={() => startZoomHold("in")}
            onPointerUp={stopZoomHold}
            onPointerCancel={stopZoomHold}
            onPointerLeave={stopZoomHold}
            aria-label="Zoom in"
            title="Click or hold to zoom in"
          >
            +
          </button>
        </div>
      </div>

      <div
        ref={previewFrameRef}
        className="ctc-preview-scroll report-screen-scroll"
        style={{ "--ctc-preview-scale": previewScale } as React.CSSProperties}
      >
        <div className="ctc-preview-center">
          <div className="ctc-preview-scale">{reportPage}</div>
        </div>
      </div>
    </div>
  );
}

