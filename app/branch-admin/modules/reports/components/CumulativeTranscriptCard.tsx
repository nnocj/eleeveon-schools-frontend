// ======================================================
// HELPERS
// ======================================================

function firstText(...values: unknown[]) {
  for (const value of values) {
    const text = String(value ?? "").trim();
    if (text) return text;
  }
  return "";
}

function templateCodeOf(
  template?: CumulativeTranscriptTemplateLike | null,
  settings?: CumulativeTranscriptSettingsLike | null
) {
  return firstText(
    template?.code,
    template?.templateCode,
    template?.layoutKey,
    template?.templateKey,
    settings?.templateCode,
    settings?.layoutKey,
    settings?.templateKey,
    DEFAULT_CUMULATIVE_TRANSCRIPT_SETTINGS.templateCode
  );
}

function mergeTranscriptSettings(args: {
  template?: CumulativeTranscriptTemplateLike | null;
  settings?: CumulativeTranscriptSettingsLike | null;
  compact?: boolean;
}) {
  const registryItem = getCumulativeTranscriptTemplateRegistryItem(
    templateCodeOf(args.template, args.settings)
  );

  const selectedCode =
    registryItem?.code ||
    args.template?.code ||
    args.template?.templateCode ||
    args.template?.layoutKey ||
    args.settings?.templateCode ||
    DEFAULT_CUMULATIVE_TRANSCRIPT_SETTINGS.templateCode ||
    "cumulative_transcript_classic";

  return {
    ...DEFAULT_CUMULATIVE_TRANSCRIPT_SETTINGS,
    ...(args.settings || {}),
    reportType: "cumulative_transcript",
    templateName:
      args.template?.name ||
      args.settings?.templateName ||
      registryItem?.name ||
      DEFAULT_CUMULATIVE_TRANSCRIPT_SETTINGS.templateName,
    templateCode: selectedCode,
    layoutKey: args.template?.layoutKey || args.settings?.layoutKey || selectedCode,
    templateKey: args.template?.templateKey || args.settings?.templateKey || selectedCode,
    paperSize: args.template?.paperSize || args.settings?.paperSize || "A4",
    orientation: args.template?.orientation || args.settings?.orientation || "portrait",
    density:
      args.settings?.density ||
      args.template?.density ||
      (args.compact ? "compact" : DEFAULT_CUMULATIVE_TRANSCRIPT_SETTINGS.density),
  };
}

function emptyTranscriptMessage(dataset: any) {
  if (!dataset) return "No cumulative transcript dataset was provided.";
  return "No cumulative transcript records are available for the selected student or filters.";
}

function datasetStudentTitle(dataset: any) {
  return firstText(
    dataset?.student?.studentName,
    dataset?.student?.fullName,
    dataset?.studentName,
    dataset?.summary?.studentName,
    "Cumulative Transcript"
  );
}

function datasetSubtitle(dataset: any) {
  return firstText(
    dataset?.student?.admissionNumber,
    dataset?.student?.studentId,
    dataset?.summary?.programmeName,
    dataset?.summary?.className,
    dataset?.branding?.schoolName,
    dataset?.header?.branding?.schoolName,
    "Academic Records"
  );
}

